@Composable
private fun IconItem(name: String, icon: Icon) {
    Column(
        Modifier.width(80.dp).innerContentPadding(),
        horizontalAlignment = Alignment.CenterHorizontally,
    ) {
        Icon(icon, name)
        Text(name, modifier = Modifier.fillMaxWidthStretch())
    }
}

@Composable
private fun IconGrid(icons: List<Pair<String, Icon>>, searchQuery: String) {
    val filtered = if (searchQuery.isBlank()) icons
    else icons.filter { it.first.contains(searchQuery, ignoreCase = true) }
    val chunked = filtered.chunked(4)
    for (row in chunked)
        Row {
            for ((name, icon) in row)
                IconItem(name, icon)
        }
}

@Composable
private fun IconSection(title: String, icons: List<Pair<String, Icon>>, searchQuery: String) {
    val filtered = if (searchQuery.isBlank()) icons
    else icons.filter { it.first.contains(searchQuery, ignoreCase = true) }
    if (filtered.isNotEmpty()) {
        Text(title, modifier = contentPaddingModifier)
        IconGrid(icons, searchQuery)
    }
}

@Composable
fun MaterialIcons() {
    var searchQuery by remember { mutableStateOf("") }
    Scaffold(
        Modifier.fillMaxSizeStretch(),
        topBar = {
            TopAppBar(
                title = { Text("Material Icons") },
            )
        },
    ) {
        VerticalScrollColumn(Modifier.fillMaxSizeStretch()) {
            OutlinedTextFieldWithMaterialIcons(
                value = searchQuery,
                onValueChange = { searchQuery = it },
                modifier = contentPaddingModifier.fillMaxWidthStretch(),
                label = "Search icons",
                leadingIcon = Icons.Filled.Search,
                trailingIcon = if (searchQuery.isNotEmpty()) Icons.Filled.Close else null,
            )
            IconSection("`material-icons-core` Filled", coreFilledIcons, searchQuery)
            IconSection("`material-icons-core` AutoMirrored Filled", coreAutoMirroredFilledIcons, searchQuery)
            IconSection("`material-icons-extended` Filled", extendedOnlyFilledIcons, searchQuery)
            IconSection("`material-icons-extended` AutoMirrored Filled", extendedOnlyAutoMirroredFilledIcons, searchQuery)
        }
    }
}
